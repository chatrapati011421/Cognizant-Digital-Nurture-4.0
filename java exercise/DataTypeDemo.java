public class DataTypeDemo {
	public static void main(String[] args) {
		int age = 25;
		float height = 5.9f;
		double weight = 72.5;
		char grade = 'A';
		boolean isStudent = true;
		System.out.println("Integer (int): " + age);
		System.out.println("Float (float): " + height);
		System.out.println("Double (double): " + weight);
		System.out.println("Character (char): " + grade);
		System.out.println("Boolean (boolean): " + isStudent);
	}
}
